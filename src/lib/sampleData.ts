function getRandomElement<T>(array: T[]): T {
  return array[Math.floor(Math.random() * array.length)];
}

function getRandomAge(minAge: number = 18, maxAge: number = 60): number {
  return Math.floor(Math.random() * (maxAge - minAge + 1)) + minAge;
}

export function generateSampleParticipants(count: number = 100) {
  const participants = [];
  
  // สร้างข้อมูลแบ่งเป็นชายหญิงประมาณ 50:50
  for (let i = 0; i < count; i++) {
    const isMale = i < count / 2; // ครึ่งแรกชาย ครึ่งหลังหญิง
    const gender = isMale ? "ชาย" : "หญิง";
    const firstName = getRandomElement(isMale ? thaiFirstNames.male : thaiFirstNames.female);
    const lastName = getRandomElement(thaiLastNames);
    const name = `${firstName} ${lastName}`;
    
    // สร้างอายุแบบกระจายตัวในแต่ละช่วง
    let age: number;
    const ageGroup = Math.floor(Math.random() * 4);
    switch (ageGroup) {
      case 0: // ต่ำกว่า 20
        age = getRandomAge(15, 19);
        break;
      case 1: // 20-29
        age = getRandomAge(20, 29);
        break;
      case 2: // 30-39
        age = getRandomAge(30, 39);
        break;
      default: // 40+
        age = getRandomAge(40, 65);
    }
    
    participants.push({ name, gender, age });
  }
  
  // สุ่มลำดับเพื่อไม่ให้เรียงตามเพศ
  return participants.sort(() => Math.random() - 0.5);
}
